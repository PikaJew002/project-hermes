    /**
     * Determine whether the user can view the model.
     *
     * @param  \App\User  $user
     * @param  \App\User  $model
     * @return mixed
     */
    public function view(User $user, User $model)
    {
        // case: user can access their own model
        if($user->id == $model->id) {
            return true;
        }
        // case: only admins of a family where the
        // model user is associated and confirmed can
        // view the model user
        foreach($user->families as $family) {
            foreach($family->bills as $bill) {
                foreach($bill->users as $billUser) {
                    if($billUser->id == $model->id
                      && $billUser->pivot->confirmed == true) {
                        return true;
                    }
                }
            }
        }
        return false;
    }

